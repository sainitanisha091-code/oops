public class method_4 {
    static void check(int n) {
        if (n % 2 == 0) {
            System.out.println("even");
        } else {
            System.out.println("odd");
        }
    }

    public static void main(String[] args) {
        method_4 obj = new method_4();
        obj.check(5);

    }
}
